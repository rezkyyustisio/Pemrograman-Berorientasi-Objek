public class OperatorKonkatenasiString {
    public static void main(String args[]){
        System.out.println("Operator Konkatenasi String (+)"); 
        System.out.println("\"Andi\" + \"Budiman\" = " + ("Andi" + "Budiman"));  
        System.out.println("\"Andi \" + \"Wijaya\" = " + ("Andi " + "Wijaya"));  
        System.out.println("\"Nilai:\" + 6 = " + ("Nilai:" + 6)); 
        System.out.println("\"6\" + 6 = " + ("6" + 6)); 
        System.out.println("6 + \"5\" = " + (5 + "6")); 
    }
}
